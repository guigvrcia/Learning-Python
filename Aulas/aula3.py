# Tipo str (String);
# String: são textos que estão dentro de aspas simples ou duplas;

print(1234)

# Aspas simples;
print('Harry Styles')

# Aspas duplas;
print("Harry Styles")

# Escape
print("Harry \"Styles")

# r;
print(r"Harry \"Styles\"")

# Aspas duplas dentro de aspas simples é possível;
print(1, 'Harry "Styles"')
print(2, "Harry 'Styles'") # Inversão também possível;
